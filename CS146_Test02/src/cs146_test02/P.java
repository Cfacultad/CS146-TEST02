
package cs146_test02;

/**
 * The sole reason this file exists is to use to avoid having to type out
 * System.out.println(); every single time I want to print something.
 * Instead, I just have to write P.pp();
 * @author Christian
 */
public class P {
    public static void p(Object x)
    {
        System.out.print(x);
    }
    public static void pp(Object x)
    {
        System.out.println(x);
    }
}
